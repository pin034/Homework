d = int(input("Введите номер дня:\n"))
if d == 1:
    print("Это понедельник.")
elif d == 2:
    print("Это вторник.")
elif d == 3:
    print("Это среда.")
elif d == 4:
    print("Это четверк.")
elif d == 5:
    print("Это пятница.")
elif d == 6:
    print("Это суббота.")
elif d == 7:
    print("Это воскресенье.")
else:
    print("Невернвый номер дня.")
